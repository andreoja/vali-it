package ee.bcs.vali.it;

public class Employees {
    private String eesnimi;
    private String pereknimi;
    private int vanus;
    private boolean oskab;

    public String getEesnimi() {
        return eesnimi;
    }

    public void setEesnimi(String eesnimi) {
        this.eesnimi = eesnimi;
    }

    public String getPereknimi() {
        return pereknimi;
    }

    public void setPereknimi(String pereknimi) {
        this.pereknimi = pereknimi;
    }

    public int getVanus() {
        return vanus;
    }

    public void setVanus(int vanus) {
        this.vanus = vanus;
    }

    public boolean isOskab() {
        return oskab;
    }

    public void setOskab(boolean oskab) {
        this.oskab = oskab;
    }


}
